CREATE TABLE IF NOT EXISTS roles (

 id   VARCHAR(255) NOT NULL,
 code VARCHAR(255) NOT NULL,
 name VARCHAR(255) NOT NULL,

 PRIMARY KEY (id),
 UNIQUE KEY UIDX_ROLES_01 (code),
 UNIQUE KEY UIDX_ROLES_02 (name)           
 
) ENGINE=InnoDB;

DELIMITER //

CREATE TRIGGER TRG_BI_ROLES

BEFORE INSERT ON roles

FOR EACH ROW

BEGIN
    SET new.id = uuid();
END
