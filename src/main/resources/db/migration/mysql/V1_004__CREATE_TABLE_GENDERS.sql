CREATE TABLE IF NOT EXISTS genders (

 id   VARCHAR(255) NOT NULL,
 code VARCHAR(255) NOT NULL,
 name VARCHAR(255) NOT NULL,

 PRIMARY KEY (id),
 UNIQUE KEY UIDX_GENDERS_01 (code) ,
 UNIQUE KEY UIDX_GENDERS_02 (name)            
 
) ENGINE=InnoDB;

DELIMITER //

CREATE TRIGGER TRG_BI_GENDERS

BEFORE INSERT ON genders

FOR EACH ROW

BEGIN
    SET new.id = uuid();
END
